    public void updateUI(@NotNull final P4ProjectConfigComponent component) {
        // Need to get the user parts, and add them in reverse order.
        // This is because the logic around "addConfigPart" always sticks the
        // newly added part to the top of the list.
        final ArrayList<ConfigPart> parts = new ArrayList<ConfigPart>(component.getUserConfigParts());
        Collections.reverse(parts);

        if (ApplicationManager.getApplication().isDispatchThread()) {
            componentList.removeAllChildren();

            for (ConfigPart part : parts) {
                addConfigPart(part);
            }
        } else {
            ApplicationManager.getApplication().invokeLater(new Runnable() {
                @Override
                public void run() {
                    componentList.removeAllChildren();

                    for (ConfigPart part : parts) {
                        addConfigPart(part);
                    }
                }
            });
        }
    }

    public void loadFromUI(@NotNull final P4ProjectConfigComponent component) {
        updateConfigPartFromUI();
        final List<ConfigPartPanel<?>> partPanels = componentList.getChildren();
        ArrayList<ConfigPart> parts = new ArrayList<ConfigPart>(partPanels.size());
        for (ConfigPartPanel<?> cmp : partPanels) {
            cmp.updateConfigPartFromUI();
            parts.add(cmp.copyPart());
        }
        component.setUserConfigParts(parts);
    }

    public boolean isModified(@NotNull P4ProjectConfigComponent configComponent) {
        final List<ConfigPart> originalParts = configComponent.getUserConfigParts();
        final List<ConfigPartPanel<?>> partPanels = componentList.getChildren();
        if (originalParts.size() != partPanels.size()) {
            return true;
        }
        final Iterator<ConfigPart> origs = originalParts.iterator();
        final Iterator<ConfigPartPanel<?>> nows = partPanels.iterator();
        while (origs.hasNext() && nows.hasNext()) {
            ConfigPart orig = origs.next();
            ConfigPartPanel<?> now = nows.next();
            if (isModified(now, orig)) {
                return true;
            }
        }
        return false;
    }

    private <T extends ConfigPart> boolean isModified(@NotNull ConfigPartPanel<T> panel, @NotNull ConfigPart part) {
        T casted = panel.castAs(part);
        return casted == null || panel.isModified(casted);
    }


    @Override
    public void dispose() {
        this.componentList.removeAllChildren();
        if (tempConfig != null) {
            Disposer.dispose(tempConfig);
        }
        this.project = null;
        this.tempConfig = null;
    }

    @NotNull
    @Override
    public P4ProjectConfig updateConfigPartFromUI() {
        final List<ConfigPartPanel<?>> partPanels = componentList.getChildren();
        ArrayList<ConfigPart> parts = new ArrayList<ConfigPart>(partPanels.size());
        for (ConfigPartPanel<?> configPartPanel : componentList.getChildren()) {
            configPartPanel.updateConfigPartFromUI();
            configPartPanel.getConfigPart().reload();
            parts.add(configPartPanel.copyPart());
        }
        if (LOG.isDebugEnabled()) {
            LOG.debug("Creating project config with " + parts);
        }
        P4ProjectConfigStack ret = new P4ProjectConfigStack(project, parts);
        ret.refresh();
        return ret;
    }

    /**
     * Called as part of the initialization from a project config.  The listener must be invoked
     * after all these are created.
     *
     * @param part part to add
     */
    private void addConfigPart(@NotNull ConfigPart part) {
        final Class<? extends ConfigPart> partClass = part.getClass();
        for (ConfigPartType configPartType : ConfigPartType.values()) {
            if (configPartType.partClass.equals(partClass)) {
                addConfigPartPanel(configPartType.createPanel(project, part));
                return;
            }
        }
        throw new IllegalArgumentException("Unexpected config part " + part);
    }

    private void addConfigPartType(@NotNull ConfigPartType type) {
        addConfigPartPanel(type.createPanel(project, null));
    }

    private <T extends ConfigPart> void addConfigPartPanel(@NotNull final ConfigPartPanel<T> panel) {
        // panel.getRootPanel().setMaximumSize(new Dimension(-1, panel.getRootPanel().getMinimumSize().height));
        panel.setRequestConfigurationLoadListener(this);
        componentList.addChildAt(0, panel);
        ApplicationManager.getApplication().executeOnPooledThread(new Runnable() {
            @Override
            public void run() {
                panel.getConfigPart().reload();
                sendConfigStackUpdated();
            }
        });
    }

    private void sendConfigStackUpdated() {
        // The refresh can take a long time, so run it in the background.
        if (!Disposer.isDisposed(this)) {
            BackgroundAwtActionRunner
                    .runBackgroundAwtAction(new BackgroundAwtActionRunner.BackgroundAwtAction<P4ProjectConfig>() {
                        @Override
                        public P4ProjectConfig runBackgroundProcess() {
                            final List<ConfigPartPanel<?>> partComponents = componentList.getChildren();
                            ArrayList<ConfigPart> parts = new ArrayList<ConfigPart>(partComponents.size());
                            for (ConfigPartPanel<?> configPartPanel : partComponents) {
                                parts.add(configPartPanel.getConfigPart());
                            }

                            if (tempConfig != null) {
                                Disposer.dispose(tempConfig);
                                tempConfig = null;
                            }
                            final P4ProjectConfig config;
                            if (project == null) {
                                config = null;
                            } else {
                                config = new P4ProjectConfigStack(project, parts);
                                config.refresh();
                            }
                            return config;
                        }

                        @Override
                        public void runAwtProcess(final @Nullable P4ProjectConfig config) {
                            // Yes, this performs extra work that shouldn't be in the AWT;
                            // however, because of the slowness of the background process, we
                            // want to wait on performing dispose checks until the last possible
                            // moment.
                            if (config != null) {
                                if (Disposer.isDisposed(ConfigStackPanel.this)) {
                                    Disposer.dispose(config);
                                } else {
                                    Disposer.register(ConfigStackPanel.this, config);
                                    for (ConfigurationUpdatedListener changeListener : changeListeners) {
                                        changeListener.onConfigurationUpdated(config);
                                    }
                                    tempConfig = config;
                                }
                            }
                        }
                    });
        }
    }


    // CalledInAWT
    private void chooseEntry() {
        final ListPopup popup = createConfigPartPopup(new Consumer<ConfigPartType>() {
            @Override
            public void consume(ConfigPartType configPartType) {
                if (configPartType != null) {
                    addConfigPartType(configPartType);
                }
            }
        });
        popup.showUnderneathOf(addEntryButton);

    }


    private ListPopup createConfigPartPopup(final Consumer<ConfigPartType> onChosen) {
        return JBPopupFactory.getInstance().createListPopup(new BaseListPopupStep<ConfigPartType>(
                P4Bundle.getString("configuration.stack.choose.title"),
                ConfigPartType.values()) {
            @Nullable
            @Override
            public PopupStep onChosen(ConfigPartType configPartType, boolean finalChoice) {
                onChosen.consume(configPartType);
                return super.onChosen(configPartType, finalChoice);
            }

            @Override
            public void canceled() {
                onChosen.consume(null);
            }

            @Nullable
            @Override
            public Icon getIconFor(ConfigPartType configPartType) {
                return configPartType.icon;
            }

            @NotNull
            @Override
            public String getTextFor(ConfigPartType configPartType) {
                return configPartType.title;
            }
        });
    }


